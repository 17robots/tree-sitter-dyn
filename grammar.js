const PREC = {
  assignment: 1,
  orElse: 2,
  range: 3,
  logicalOr: 4,
  logicalAnd: 5,
  bitOr: 6,
  bitXor: 7,
  bitAnd: 8,
  equality: 9,
  shift: 10,
  additive: 11,
  multiplicative: 12,
  unary: 13,
  call: 14,
  functionLiteral: 9,
}
module.exports = grammar({
  name: "dyn",
  word: ($) => $.identifier,
  extras: ($) => [/[\s\uFEFF\u2060\u200B]/, $.line_comment, $.block_comment],
  conflicts: ($) => [
    [$.block_expression, $.tuple_expression],
    [$.call_argument, $.parenthesized_expression],
    [$.binary_expression, $.function_expression],
    [$.primary_expression, $.function_parameter],
    [$.primary_expression, $.labeled_block_expression],
    [$.primary_expression, $.struct_literal],
    [$.extern_function_type]
  ],
  rules: {
    source_file: $ => seq(optional($.module_declaration), repeat($.declaration)),
    module_declaration: $ => seq('module', field('name', $.identifier)),
    declaration: $ => seq(
      repeat($.doc_comment),
      optional('pub'),
      choice($.extern_binding_declaration, $.binding_declaration),
    ),
    extern_binding_declaration: $ => seq(
      optional("mut"),
      field("name", $.identifier),
      choice(seq(":", "="), "="),
      field("signature", $.extern_function_signature),
    ),
    extern_function_signature: $ => seq(
      "extern",
      field("type", choice($.extern_function_type, $.type_expression)),
      optional(seq("=", field("link_name", $.string_literal))),
    ),
    extern_function_type: $ => seq(
      "(",
      optional(seq(commaSep1($.function_type_parameter), optional(","))),
      ")",
      optional(field("return_type", $.type_expression)),
    ),
    binding_declaration: $ => seq(
      optional("mut"),
      field("name", $.identifier),
      choice(
        seq(":", "=", field("value", $.expression)),
        seq(":", field("type", $.type_expression), "=", field("value", $.expression)),
        seq("=", field("value", $.expression)),
      ),
    ),
    statement: $ => choice($.local_binding_statement, $.expression_statement, alias(";", $.empty_statement)),
    local_binding_statement: $ => prec.right(
      seq(
        repeat($.doc_comment),
        optional("pub"),
        optional("mut"),
        field("name", $.identifier),
        choice(
          seq(":", "=", field("value", $.expression)),
          seq(":", field("type", $.type_expression), "=", field("value", $.expression)),
          seq("=", field("value", $.expression)),
        ),
        optional(";"),
      ),
    ),
    expression_statement: $ => seq($.expression, ";"),
    expression: $ => choice(
      $.if_expression,
      $.match_expression,
      $.for_expression,
      $.break_expression,
      $.continue_expression,
      $.return_expression,
      $.defer_expression,
      $.comptime_expression,
      $.inline_expression,
      $.use_expression,
      $.assignment_expression,
      $.or_else_expression,
      $.binary_expression,
      $.unary_expression,
      $.postfix_expression,
      $.primary_expression,
    ),
    if_expression: $ => prec.right(
      seq(
        "if",
        field("condition", $.expression),
        optional(seq(":", field("capture", $.pipe_binding))),
        field("consequence", $.expression),
        optional(seq("else", field("alternative", $.expression))),
      ),
    ),
    match_expression: $ => seq(
      "match",
      field("scrutinee", $.expression),
      "{",
      optional(seq(commaSep1($.match_arm), optional(","))),
      "}",
    ),
    match_arm: $ => seq(
      field("pattern", $.pattern),
      optional(seq("if", field("guard", $.expression))),
      choice(":", "=>"),
      optional(field("capture", $.pipe_binding)),
      field("value", $.expression),
    ),
    for_expression: $ => choice(
      prec(2, seq("for", field("body", $.block_expression))),
      prec(
        1,
        seq(
          "for",
          field("start", $.expression),
          field("range_operator", choice("..", "..=")),
          field("end", $.expression),
          ":",
          optional(field("binding", $.pipe_binding)),
          field("body", $.expression),
        ),
      ),
      prec(
        1,
        seq(
          "for",
          field("iterable_or_condition", $.expression),
          ":",
          optional(field("binding", $.pipe_binding)),
          field("body", $.expression),
        ),
      ),
    ),
    break_expression: $ => prec.right(
      seq(
        "break",
        optional(seq(":", field("label", $.identifier))),
        optional(field("value", $.expression)),
      ),
    ),
    continue_expression: $ => prec.right(seq("continue", optional(seq(":", field("label", $.identifier))))),
    return_expression: $ => prec.right(seq("return", optional(field("value", $.expression)))),
    defer_expression: $ => seq("defer", optional(field("binding", $.pipe_binding)), field("body", $.expression)),
    comptime_expression: $ => seq("comp", field("expression", $.expression)),
    inline_expression: $ => seq("inline", field("expression", $.expression)),
    use_expression: $ => seq("use", field("path", $.string_literal)),
    assignment_expression: $ => prec.right(
      PREC.assignment,
      seq(
        field("target", $.postfix_expression),
        field(
          "operator",
          choice(
            "=",
            "+=",
            "-=",
            "*=",
            "/=",
            "%=",
            "&=",
            "|=",
            "^=",
            "<<=",
            ">>=",
          ),
        ),
        field("value", $.expression),
      ),
    ),
    or_else_expression: $ => prec.left(
      PREC.orElse,
      seq(
        field("value", $.expression),
        "or",
        optional(field("binding", $.pipe_binding)),
        field("fallback", $.expression),
      ),
    ),
    binary_expression: $ => choice(
      prec.right(PREC.range, seq($.expression, choice("..", "..="), $.expression)),

      prec.left(PREC.logicalOr, seq($.expression, "||", $.expression)),
      prec.left(PREC.logicalAnd, seq($.expression, "&&", $.expression)),

      prec.left(PREC.bitOr, seq($.expression, "|", $.expression)),
      prec.left(PREC.bitXor, seq($.expression, "^", $.expression)),
      prec.left(PREC.bitAnd, seq($.expression, "&", $.expression)),

      prec.left(
        PREC.equality,
        seq($.expression, choice("==", "!=", "<", "<=", ">", ">="), $.expression),
      ),

      prec.left(PREC.shift, seq($.expression, choice("<<", ">>"), $.expression)),

      prec.left(PREC.additive, seq($.expression, choice("+", "-"), $.expression)),

      prec.left(PREC.multiplicative, seq($.expression, choice("*", "/", "%"), $.expression)),
    ),
    unary_expression: $ => prec(PREC.unary, seq(field("operator", choice("-", "!", "~", "&")), $.expression)),
    postfix_expression: $ => choice(
      $.call_expression,
      $.subscript_expression,
      $.field_expression,
      $.deref_access_expression,
      $.optional_unwrap_expression,
      $.error_unwrap_expression,
    ),
    call_expression: $ => prec.left(PREC.call, seq(field("function", $.expression), $.argument_list)),
    argument_list: $ => seq("(", optional(seq(commaSep1($.call_argument), optional(","))), ")"),
    call_argument: $ => $.expression,
    subscript_expression: $ => prec.left(
      PREC.call,
      seq(
        field("base", $.expression),
        "[",
        choice(
          field("index", $.expression),
          seq(
            optional(field("start", $.expression)),
            field("range_operator", choice("..", "..=")),
            optional(field("end", $.expression)),
          ),
        ),
        "]",
      ),
    ),
    field_expression: $ => prec.left(PREC.call, seq(field("base", $.expression), ".", field("field", $.identifier))),
    deref_access_expression: $ => prec.left(PREC.call, seq(field("base", $.expression), ".*")),
    optional_unwrap_expression: $ => prec.left(PREC.call, seq(field("base", $.expression), ".?")),
    error_unwrap_expression: $ => prec.left(PREC.call, seq(field("base", $.expression), ".!")),
    primary_expression: $ => choice(
      $.identifier,
      $.builtin_identifier,
      $.integer_literal,
      $.float_literal,
      $.string_literal,
      $.char_literal,
      $.boolean_literal,
      $.null_literal,
      $.parenthesized_expression,
      $.function_expression,
      $.array_literal,
      $.tuple_expression,
      $.labeled_block_expression,
      $.block_expression,
      $.struct_literal,
      $.enum_variant_expression,
      $.type_literal_expression,
    ),
    parenthesized_expression: $ => seq("(", $.expression, ")"),
    function_expression: $ => prec(
      PREC.functionLiteral,
      seq(
        "(",
        optional(seq(commaSep1($.function_parameter), optional(","))),
        ")",
        optional(field("return_type", $.type_expression)),
        optional(seq("!", field("errors", $.error_type_list))),
        field("body", choice(seq("=>", $.expression), $.block_expression)),
      ),
    ),
    function_parameter: $ => seq(
      field("name", $.identifier),
      optional(seq(":", field("type", $.type_expression))),
      optional(seq("=", field("default", $.expression))),
    ),
    array_literal: $ => seq("[", optional(seq(commaSep1($.expression), optional(","))), "]"),
    tuple_expression: $ => seq("{", commaSep1($.expression), optional(","), "}"),
    block_expression: $ => seq("{", repeat($.statement), optional(field("tail", $.expression)), "}"),
    labeled_block_expression: $ => seq(field("label", $.identifier), ":", field("body", $.block_expression)),
    struct_literal: $ => choice(
      seq(field("name", $.identifier), $.struct_literal_body),
      seq(".", $.struct_literal_body),
    ),
    struct_literal_body: $ => seq("{", optional(seq(commaSep1($.struct_literal_field), optional(","))), "}"),
    struct_literal_field: $ => seq(field("name", $.identifier), ":", field("value", $.expression)),
    enum_variant_expression: $ => choice(
      prec(1, seq(".", field("variant", $.identifier), field("payload", $.argument_list_values))),
      seq(".", field("variant", $.identifier)),
    ),
    argument_list_values: $ => seq("(", optional(seq(commaSep1($.expression), optional(","))), ")"),
    type_literal_expression: $ => choice($.struct_type, $.enum_type),
    pattern: $ => choice(
      $.wildcard_pattern,
      $.range_pattern,
      $.enum_pattern,
      $.literal_pattern,
      $.identifier_pattern,
    ),
    wildcard_pattern: _ => "_",
    range_pattern: $ => prec.right(
      seq(
        field("start", choice($.literal_pattern, $.identifier_pattern)),
        field("operator", choice("..", "..=")),
        field("end", $.pattern),
      ),
    ),
    enum_pattern: $ => choice(
      seq(".", field("variant", $.identifier), optional($.pattern_binding_list)),
      seq(
        field("root", $.identifier),
        ".",
        field("variant", $.identifier),
        optional($.pattern_binding_list),
      ),
    ),
    pattern_binding_list: $ => seq("(", optional(seq(commaSep1(choice($.identifier, "_")), optional(","))), ")"),
    identifier_pattern: $ => $.identifier,
    literal_pattern: $ => choice(
      $.integer_literal,
      $.float_literal,
      $.string_literal,
      $.char_literal,
      $.boolean_literal,
      $.null_literal,
    ),
    type_expression: $ => prec.right(
      choice(
        seq(choice("comp", "inline"), $.type_expression),
        seq("?", $.type_expression),
        seq("*", optional("mut"), $.type_expression),
        seq("[", "]", optional("mut"), $.type_expression),
        seq("[", $.expression, "]", $.type_expression),
        seq($.base_type_expression, "!", $.error_type_list),
        $.base_type_expression,
      ),
    ),
    base_type_expression: $ => choice($.function_type, $.struct_type, $.enum_type, $.applied_type, $.named_type, 'type'),
    named_type: $ => field("name", $.identifier),
    applied_type: $ => seq(
      field("callee", $.identifier),
      "(",
      optional(seq(commaSep1($.type_expression), optional(","))),
      ")",
    ),
    function_type: $ => prec.right(
      seq(
        "fn",
        "(",
        optional(seq(commaSep1($.function_type_parameter), optional(","))),
        ")",
        optional(field("return_type", $.type_expression)),
      ),
    ),
    function_type_parameter: $ => choice(
      seq(field("name", $.identifier), ":", field("type", $.type_expression)),
      field("type", $.type_expression),
    ),
    struct_type: $ => seq(
      optional("packed"),
      "struct",
      "{",
      optional(seq(commaSep1($.struct_type_member), optional(","))),
      "}",
    ),
    struct_type_member: $ => choice(
      seq(field("name", $.identifier), ":", field("type", $.type_expression)),
      seq(field("name", $.identifier), ":", "=", field("value", $.expression)),
      seq(field("name", $.identifier), "=", field("value", $.expression)),
    ),
    enum_type: $ => seq(
      "enum",
      optional(seq("(", field("repr", $.type_expression), ")")),
      "{",
      optional(seq(commaSep1($.enum_type_variant), optional(","))),
      "}",
    ),
    enum_type_variant: $ => seq(field("name", $.identifier), optional(seq(":", field("payload", $.type_expression)))),
    error_type_list: $ => prec.right(seq($.identifier, repeat(seq(",", $.identifier)))),
    pipe_binding: $ => seq("|", choice($.identifier, "_"), "|"),
    identifier: _ => /[A-Za-z_][A-Za-z0-9_]*/,
    builtin_identifier: _ => /\$[A-Za-z_][A-Za-z0-9_]*/,
    integer_literal: _ => token(choice(/0[bB][01][01_]*/, /0[oO][0-7][0-7_]*/, /0[xX][0-9a-fA-F][0-9a-fA-F_]*/, /[0-9][0-9_]*/)),
    float_literal: _ => token(
      choice(
        /[0-9][0-9_]*\.[0-9][0-9_]*([eE][+-]?[0-9][0-9_]*)?/,
        /[0-9][0-9_]*[eE][+-]?[0-9][0-9_]*/,
        /0[xX][0-9a-fA-F][0-9a-fA-F_]*\.[0-9a-fA-F_]*[pP][+-]?[0-9][0-9_]*/,
        /0[xX][0-9a-fA-F][0-9a-fA-F_]*[pP][+-]?[0-9][0-9_]*/,
      ),
    ),
    string_literal: _ => token(seq('"', repeat(choice(/[^"\\\n]/, /\\./)), '"')),
    char_literal: _ => token(seq("'", choice(/[^'\\\n]/, /\\./), "'")),
    boolean_literal: _ => choice("true", "false"),
    null_literal: _ => "null",
    doc_comment: _ => token(prec(2, seq("///", /[^\n]*/))),
    line_comment: _ => token(prec(1, seq("//", /[^\n]*/))),
    block_comment: _ => token(/\/\*[^*]*\*+([^/*][^*]*\*+)*\//),
  },
})
function commaSep1(rule) {
  return seq(rule, repeat(seq(",", rule)))
}

